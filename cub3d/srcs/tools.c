#include "../includes/cub3d.h"


void			ft_space_skip(char **s)
{
	while ((**s >= 9 && **s <= 13) || **s == ' ')
		++*s;
}

void			ft_skip(char **s, char c)
{
	ft_space_skip(s);
	if (**s == c)
		++*s;
	ft_space_skip(s);
}